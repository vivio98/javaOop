package syntax;

public class SwingSyntax {
	/*
	 * 스윙(swing)은 AWT의 제약을 극복하기 위하여 만들어 졌다.
	 * 자바에서 GUI를 구현하기 위해 JDK에서 기본적으로 제공하는
	 * 개발 툴킷으로 자바기반의 클래스의 일부이다.
	 * 스윙은 기존의 발표되었던 AWT(Abstract window Toolkit)가 OS및 윈도우 시스템의 자원을 그대로 제공받기 때문에
	 * 자바에서 제향하는 Write Once, Run Everywhere(WORE)을
	 * 구현하기 위해 각종시스템에서 공통적으로 제공하는 버튼, 대화창들만 구현하고 표나 트리등의 좀 더 복잡하고 
	 * 다양한 그래픽 컴포넌트를 사용 할 수 없다는 단점을 보완하기 위해 JDK1.2버젼부터 사용했다.
	 */

}
